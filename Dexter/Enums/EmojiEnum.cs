namespace Dexter.Enums
{

	/// <summary>
	/// The EmojiEnum specifies the type of Embed you wish to be genereated by the builder.
	/// </summary>

	public enum EmojiEnum
	{

		/// <summary>
		/// The UNKNOWN value will have no thumbnail and the embed will be ORANGE.
		/// </summary>

		Unknown,

		/// <summary>
		/// The ANNOYED value will have an annoyed thumbnail and will be RED.
		/// </summary>

		Annoyed,

		/// <summary>
		/// The LOVE value will have a thumbnail with hearts and will be GREEN.
		/// </summary>

		Love,

		/// <summary>
		/// The WUT value will be a confused thumbnail and will be TEAL.
		/// </summary>

		Wut,

		/// <summary>
		/// The SIGN value will be an image of a sign and will be BLUE.
		/// </summary>

		Sign

	}

}
